# Simple for loop

fruits = ["Apple", "Peach", "Pear"]
for fruit in fruits:
    print(fruit)
    print(fruit + " Pie")
    print(fruits)

vegetables = ["Carrot", "Ladies finger", "Potato"]
for x in vegetables[1]:
    print(x)
for y in vegetables[-3]:
    print(y)
for vegetable in vegetables:
    print(vegetable)







